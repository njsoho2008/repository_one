package ch3;

public class BasicDataTypeDemo {
    public static void main(String[] args){
        double  myd=1.0/3;
        System.out.println("myd:   "+myd);
        double  myd2=1/3;
        System.out.println("myd2:  "+myd2);
        float  myf=1.0f/3;
        System.out.println("myf:   "+myf);
        int i=0;
        System.out.println("整整值："+i);

    }

}
